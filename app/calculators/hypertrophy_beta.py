def exercise_muscle_load(sets: float, muscle_coefficient: float) -> float:
    return round(sets * muscle_coefficient, 2)


def calculate_exercise_contribution(muscle_group: str, exercise_name: str, sets: float) -> dict[str, float]:
    exercises = COEFFICIENT_MAP.get(muscle_group)
    if exercises is None or exercise_name not in exercises:
        raise ValueError("Unknown muscle group/exercise")

    result: dict[str, float] = {}
    for muscle_name, coeff in exercises[exercise_name]:
        result[muscle_name] = exercise_muscle_load(sets=sets, muscle_coefficient=float(coeff))
    return result


def weekly_muscle_score(contributions: list[dict[str, float]]) -> dict[str, float]:
    totals: dict[str, float] = defaultdict(float)
    for block in contributions:
        for muscle_name, value in block.items():
            totals[muscle_name] += value
    return {k: round(v, 2) for k, v in totals.items()}


def hypertrophy_percent(weekly_score: float) -> float:
    return round(weekly_score / 0.25, 2)
